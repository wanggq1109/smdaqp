package com.powerlong.vo;

public class ScreenVideoVo {
	private String touchId;
	private String content;
	private String cnt;
	private String daycnt;
	private String weekcnt;
	private String yearcnt;
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCnt() {
		return cnt;
	}
	public void setCnt(String cnt) {
		this.cnt = cnt;
	}
	public String getDaycnt() {
		return daycnt;
	}
	public void setDaycnt(String daycnt) {
		this.daycnt = daycnt;
	}
	public String getWeekcnt() {
		return weekcnt;
	}
	public void setWeekcnt(String weekcnt) {
		this.weekcnt = weekcnt;
	}
	public String getYearcnt() {
		return yearcnt;
	}
	public void setYearcnt(String yearcnt) {
		this.yearcnt = yearcnt;
	}
	public String getTouchId() {
		return touchId;
	}
	public void setTouchId(String touchId) {
		this.touchId = touchId;
	}
	
	
}
